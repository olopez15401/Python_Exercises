class car():
    """A much more improved class of a car"""

    def __init__(self, engine, transmission, type, year):
        self.setEngine(engine)
        self.setTransmission(transmission)
        self.setType(type)

    def setEngine(engine):
        self.engine = engine

    def setTransmission(transmission):
        self.transmission = transmission

    def setType(type):
        self.type = type

    def setYear(year):
        self.year = year

    
